package ex04_Operators;

public class Lab038_unary_operator {
    public static void main(String[] args) {
        // unary operator

        int a = 10;
        int a1 = -5;
        System.out.println(a);
        System.out.println(a1);

        int b = -4;
        b = b + 4;
        System.out.println(b);
    }
}