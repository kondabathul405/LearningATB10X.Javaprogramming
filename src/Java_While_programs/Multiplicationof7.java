package Java_While_programs;

public class Multiplicationof7 {
    public static void main(String[] args) {
        System.out.println("Multiplicationof7 :");

        int n =7;
        int m=n;
        int i=1;
        while(i<=10){
            m = 7*i;
            System.out.println("7 *" +i +" = "+m);
            i++;

        }
    }
}
