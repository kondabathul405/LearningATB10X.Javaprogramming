package ex07_Incement_Decrement_Operator;

public class Lab071_Post_ID {
    public static void main(String[] args) {
        int a= 10;
        System.out.println(++a);
        System.out.println(a);


        // POST incremenet  = Print first and then increase
        int a_post = 10;
        System.out.println(a_post++);
        System.out.println(a_post);
    }
}
